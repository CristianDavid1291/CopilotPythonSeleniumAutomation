
#Data types
a = 10  # Integer
b = 3.14  # Float
c = "Hello, World!"  # String
d = True  # Boolean

print(a,b,c,d)

#lists
my_list = [1, 2, 3, 4, 5]  # List of integers
my_list.append(6)  # Adding an element to the list
print(my_list)
print(my_list[0])  # Accessing the first element of the list
print(my_list[1:3])  # Slicing the list to get elements from index 1 to 2
print(my_list[-1])  # Accessing the last element of the list
print(my_list[1:])  # Slicing the list to get elements from index 1 to the end
print(my_list[:3])  # Slicing the list to get the first three elements
print(my_list[::2])  # Slicing the list to get every second element
print(my_list[::-1])  # Reversing the list
print(my_list[0:-1:1])  # Slicing the list with a step of 2
my_list.insert(1,"Cristian")
print(my_list)  # Printing the modified list



